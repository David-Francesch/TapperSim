public class BarBean {
    int nCervezas = 0;
    int nClientes = 0;

    public void setnCervezas(int nCervezas) {
        this.nCervezas = nCervezas;
    }

    public int getnCervezas() {
        return nCervezas;
    }

    public void setnClientes(int nClientes) {
        this.nClientes = nClientes;
    }

    public int getnClientes() {
        return nClientes;
    }
}
